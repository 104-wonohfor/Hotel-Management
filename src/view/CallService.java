    // set roomNumberBox là phòng mà user đã check in
    private void loadRoomNumberBox(){
        roomNumberBox.removeAllItems();
        List<RoomBooking> roomBookingList = new RoomBookingDAO().getCheckInRoomOfGuestID(userID);
        for (RoomBooking roomBooking : roomBookingList) {
            roomNumberBox.addItem(String.valueOf(roomBooking.getRoomNumber()));
        }
    }

    private void loadTable() {
        jTable1.setModel(new KitchenServiceDAO().getKitchenServiceList());      
    }

    private void createService(String description, double price) {
        int roomNumber = Integer.parseInt(roomNumberBox.getSelectedItem().toString());
        String bookingID = new RoomBookingDAO().getBookingID(roomNumber);

        Service service = new Service();
        service.setBookingID(Integer.parseInt(bookingID));
        service.setDescription(description);
        service.setRoomNumber(roomNumber);
        service.setServiceStatus(ServiceStatus.REQUESTED);
        service.setServiceAmount(price);

        try {
            new ServiceDAO().insertService(service);
            new RequestDAO().insertRequest("ROOM " + roomNumber + " requested a service");

            Guest guest = new Guest();
            guest.callService();

            JOptionPane.showMessageDialog(null, "Service Requested");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }

    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed
        // TODO add your handling code here:
        // if userID starts with "G" then back to DashboardGuest, with "R" then back to DashboardReceptionist, with "M" then back to DashboardManager
        if (userID.startsWith("G")) {
            new DashboardGuest(userID).setVisible(true);
        } else if (userID.startsWith("R")) {
            try {
                new DashboardReceptionist(userID).setVisible(true);
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else if (userID.startsWith("M")) {
            try {
                new DashboardManager(userID).setVisible(true);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        this.setVisible(false);
    }//GEN-LAST:event_backButtonActionPerformed

