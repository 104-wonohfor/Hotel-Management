    private void orderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderButtonActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Please select a service");
            return;
        }

        String description = jTable1.getValueAt(row, 0).toString();
        double price = Double.parseDouble(jTable1.getValueAt(row, 1).toString());
        int quantity = (int) quantitySpinner.getValue();
        String note = "";
        
        if (!noteField.getText().isEmpty()) {
            note = "Note: " + noteField.getText();
        }

        description = quantity + " " + description + " - " + note;

        try {
            createService(description, price * quantity);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage()); 
            return;
        }

        jTable1.clearSelection();
        quantitySpinner.setValue(1);
        noteField.setText("");
    }//GEN-LAST:event_orderButtonActionPerformed

