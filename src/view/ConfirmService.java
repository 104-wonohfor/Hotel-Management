    private void loadServiceTable(String choice) {
        if (choice.equals("Only Requested")) {
            choice = "REQUESTED";
        } else if (choice.equals("Only Confirmed")) {
            choice = "CONFIRMED";
        }

        ServiceDAO serviceDAO = new ServiceDAO();
        List<Service> services = serviceDAO.getAllService(choice);

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");

        String[][] data = new String[services.size()][7];
        for (int i = 0; i < services.size(); i++) {
            data[i][0] = String.valueOf(services.get(i).getServiceID());
            data[i][1] = String.valueOf(services.get(i).getBookingID());
            data[i][2] = services.get(i).getDescription();
            data[i][3] = sdf.format(services.get(i).getIssueAt());
            data[i][4] = String.valueOf(services.get(i).getRoomNumber());
            data[i][5] = services.get(i).getServiceStatus().toString();
            data[i][6] = String.valueOf(services.get(i).getServiceAmount());
        }

        jTable2.setModel(new javax.swing.table.DefaultTableModel(
                data,
                new String[]{
                    "ServiceID", "Booking ID", "Description", "Issue At", "Room Number", "Service Status", "Service Amount"
                }
        ));        
    }

    private void backButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backButtonActionPerformed
        // TODO add your handling code here:
        // if userID starts with "G" then back to DashboardGuest, with "R" then back to DashboardReceptionist, with "M" then back to DashboardManager
        if (userID.startsWith("G")) {
            new DashboardGuest(userID).setVisible(true);
        } else if (userID.startsWith("R")) {
            try {
                new DashboardManager(userID).setVisible(true);
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else if (userID.startsWith("M")) {
            try {
                new DashboardManager(userID).setVisible(true);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        this.setVisible(false);
    }//GEN-LAST:event_backButtonActionPerformed

