    private void jTable2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MousePressed
        // TODO add your handling code here:
        int row = jTable2.getSelectedRow();

        String description = jTable2.getValueAt(row, 2).toString();
        double amount = Double.parseDouble(jTable2.getValueAt(row, 6).toString());

        descriptionField.setText(description);
        amountField.setText(String.valueOf(amount));

        String bookingID = jTable2.getValueAt(row, 1).toString();
        String guestID = new RoomBookingDAO().getGuestID(Integer.parseInt(bookingID));

        Person person = new PersonDAO().getPersonByID(guestID);
        guestNameFld.setText(person.getName());
        phoneField.setText(person.getPhone());
    }//GEN-LAST:event_jTable2MousePressed

    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        // TODO add your handling code here:
        int row = jTable2.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Please select a service to confirm", "Error", JOptionPane.ERROR_MESSAGE); 
            return;
        } else {
            String serviceStatus = jTable2.getValueAt(row, 5).toString();
            if (!serviceStatus.equals("REQUESTED") && !serviceStatus.equals("CANCELLED")) {
                JOptionPane.showMessageDialog(null, "Only REQUESTED or CANCELLED service can be confirmed", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            int serviceID = Integer.parseInt(jTable2.getValueAt(row, 0).toString());
            ServiceDAO serviceDAO = new ServiceDAO();
            serviceDAO.updateServiceStatus(serviceID, ServiceStatus.CONFIRMED);
            JOptionPane.showMessageDialog(null, "Service confirmed successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadServiceTable(choiceBox.getSelectedItem().toString());
        }
    }//GEN-LAST:event_confirmButtonActionPerformed

